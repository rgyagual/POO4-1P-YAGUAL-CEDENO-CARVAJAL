/**
 * Representa las fases de los grupos en el mundial.
 * 
 * @author Yagual-Cedeño-Carvajal
 */

public enum FasesMundial {
    /** Fase de Grupos */
    GRUPOS,
    /** Octavos de Final */
    OCTAVOS,
    /** Cuartos de Final */
    CUARTOS,
    /** Semi-final */
    SEMIFINAL,
    /** Partido para Tercer Lugar */
    TERCER_LUGAR,
    /** Final del Mundial */
    FINAL,
}
